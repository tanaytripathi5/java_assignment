public class implement_operator {
    public static void main(String [] args)
    {
        // Boolean operator and relational
        boolean a = 10>15;
        System.out.println(a);
        boolean b = 10<15;
        System.out.println(b);
        // Logical operator
        System.out.println((5 > 3) && (8 > 5));
        System.out.println((5 > 3) && (8 < 5));
    }
}
